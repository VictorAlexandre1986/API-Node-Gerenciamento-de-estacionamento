class ParkingSpot {
    constructor(id, car, name, plate, entryTime, exitTime, spotNumber) {
      this.id = id;
      this.car = car;
      this.name = name;
      this.plate = plate;
      this.entryTime = entryTime;
      this.exitTime = exitTime;
      this.spotNumber = spotNumber;
    }
  }
  
  module.exports = ParkingSpot;
  